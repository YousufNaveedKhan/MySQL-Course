-- TO CREATE A STORED PROCEDURE TO INSERT VALUES
DELIMITER //
CREATE PROCEDURE INSERTDATA(STDNAME VARCHAR(250), CRSE VARCHAR(250))
BEGIN
  -- INSERT VALUES INTO THE STUDENT TABLE
  INSERT INTO STUDENT (NAME, COURSE) VALUES (STDNAME, CRSE);
END 
//

-- CALL THE STORED PROCEDURE TO INSERT VALUES
