CALL INSERTDATA("Bushra", "PHP");
CALL INSERTDATA("Sara", "PYTHON");
CALL INSERTDATA("Shahid", "PYTHON");

-- TO CREATE A STORED PROCEDURE TO DELETE VALUES
DELIMITER //
CREATE PROCEDURE DELETEDATA(STDNAME VARCHAR(250))
BEGIN 
  -- DELETE VALUES FROM THE STUDENT TABLE
  DELETE FROM STUDENT WHERE NAME = STDNAME;
END 
//

-- CALL THE STORED PROCEDURE TO DELETE VALUES
