-- TO CREATE A STORED PROCEDURE TO SEARCH DATA BY NAME
DELIMITER //
CREATE PROCEDURE SEARCHBYNAME(STDNAME VARCHAR(250))
BEGIN
  -- SELECT DATA FROM THE STUDENT TABLE BASED ON THE NAME
  SELECT * FROM STUDENT WHERE NAME = STDNAME;
END
//

-- CALL THE STORED PROCEDURE TO SEARCH DATA BY NAME
